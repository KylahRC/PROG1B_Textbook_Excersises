package Ex4;

public class Poem
{
    String poemName;
    int numLines;

    public void setNumLines(int numLines)
    {
        this.numLines = numLines;
    }

    public void setPoemName(String poemName)
    {
        this.poemName = poemName;
    }

    public String getPoemName()
    {
        return poemName;
    }

    public int getNumLines()
    {
        return numLines;
    }
}
